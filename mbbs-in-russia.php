                                    <h5 class="pt-2 pb-2">MBBS In Russia Syllabus</h5>
                                    <p class="pb-1 text-justify">The syllabus for MBBS is the same for all countries. The difference in the syllabus is only the length and sequence of the subject. Russian universities follow European pattern.</p>
                                    <p class="pb-1 text-justify"><b>First Year - </b>The first year focuses on basics. There will be Biology, Physics, Chemistry, Nursing (surgery & therapy), Anatomy, Histology, Latin, and Public Health. As an addition, students study History, Economics, Philosophy, Culturology, Russian language, Physical Training, Basics of Research and Mathematics.</p>
                                    <p class="pb-1 text-justify"><b>Second Year - </b>The second-year focuses on the functioning of the human body and metabolic processes. It includes subjects like Physiology, Biochemistry, Immunology, and Hygiene. As an addition, students study Psychology, Medical Law, Bioethics, and the Russian language for doctor-patient communication. Students must work as a nurse in the hospital for minimum two weeks, after obtaining the degree.</p>
                                    <p class="pb-1 text-justify"><b>Third Year - </b>In the third year, students undergo training in the etiology of the diseases and the action of drugs, which includes Microbiology, Pathology, Pharmacology, Radiology, Internal medicine, Topographical anatomy and Operative surgery.</p>
                                    <p class="pb-1 text-justify"><b>Fourth Year - </b>The 4th year begins with hospital cycles. The students undergo training in different departments of the hospital as part of their course. The practical class is divided into two parts, the first half comprising discussion and the second half practice in the patient ward of the department. In the fourth year, future doctors must go through the various departments in the hospitals like Surgery, Dermatology and Venereology, Obstetrics and Gynecology, Neurology, Therapy, Medical Rehabilitation, Urology, Public health, Pediatrics, phthisiology, and Endocrinology.</p>
                                    <p class="pb-1 text-justify"><b> Fifth Year - </b> In the fifth year, the students will get trained in E.N.T., Ophthalmology, Surgical Gynecology, Psychiatry, Traumatology, Orthopedics, Physiotherapy, Outpatient therapy, Gene therapy, and Infectious diseases.</p>
                                    <p class="pb-1 text-justify"><b>Sixth Year - </b>The last year of the MBBS includes Oncology and radiation therapy, Occupational diseases, Anesthesiology, Intensive care training, Dentistry, Clinical Immunology, General medical practice, Neurosurgery, Forensic medicine, and Epidemiology.</p>

